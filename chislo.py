def main():
    n = int(input("Введіть чотирицифрове число: "))

    
    d1 = n // 1000           
    d2 = (n // 100) % 10     
    d3 = (n // 10) % 10      
    d4 = n % 10              

    
    newN = d3 * 1000 + d4 * 100 + d1 * 10 + d2

    print("Нове число:", newN)

    
    print("Квадрати цифр числа:", d1**2, d2**2, d3**2, d4**2)


if __name__ == "__main__":
    main()
